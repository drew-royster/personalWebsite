let timeoutId;
const debounceDelay = 200; // Adjust the delay as needed

function debounce(fn, delay) {
  return function (...args) {
    clearTimeout(timeoutId);
    timeoutId = setTimeout(() => {
      fn(...args);
    }, delay);
  };
}

const debouncedPerformSearch = debounce(performSearch, debounceDelay);

// Watch for changes in the search query to perform searches automatically
watch(searchQuery, (newQuery, oldQuery) => {
  if (newQuery !== oldQuery) {
    debouncedPerformSearch();
  }
});

async function performSearch() {
  const trimmedQuery = searchQuery.value.trim()

  if (!trimmedQuery) {
    highlightId.value = null;
  } else {
    const pipe = await pipeline("feature-extraction", "Supabase/gte-small");

    const output = await pipe(trimmedQuery, {
      pooling: "mean",
      normalize: true,
    });

    let bestMatchId = null;
    let bestMatchScore = 0;
    for (let experience of experiences.value) {
      const matchScore = cos_sim(experience.embeddings, output.data);
      if (matchScore > bestMatchScore) {
        bestMatchScore = matchScore;
        bestMatchId = experience.id;
      }
    }

    highlightId.value = bestMatchId;
  }
}
